    private void tblCompanhiaSelect(int val) {
	txtNewSigla.setEnabled(false);
	btnDeleteCompanhia.setEnabled(true);
	tblCompanhia.changeSelection(val, 0, false, false);
	String sigla, nome, nomeFantasia, pais;
	sigla = (String) tblCompanhia.getValueAt(val, 0);
	nome = (String) tblCompanhia.getValueAt(val, 1);
	nomeFantasia = (String) tblCompanhia.getValueAt(val, 2);
	pais = (String) tblCompanhia.getValueAt(val, 3);
	txtNewSigla.setText(sigla);
	txtNewNome.setText(nome);
	txtNewNomeFantasia.setText(nomeFantasia);
	txtNewPais.setText(pais);
    }

    private void tblCompanhiaPopulate() {
	List<CompanhiaTO> res = controller.find(txtSearchCompanhia.getText());
	DefaultTableModel modelo = (DefaultTableModel) tblCompanhia.getModel();
	modelo.setRowCount(0);
	if (res == null) {
	    return;
	}
	int i = 0;
	for (CompanhiaTO companhia : res) {
	    modelo.addRow(new Object[modelo.getColumnCount()]);
	    tblCompanhia.setValueAt(companhia.getSigla(), i, 0);
	    tblCompanhia.setValueAt(companhia.getNome(), i, 1);
	    tblCompanhia.setValueAt(companhia.getNomeFantasia(), i, 2);
	    tblCompanhia.setValueAt(companhia.getPais(), i, 3);
	    i++;
	}
    }
    
