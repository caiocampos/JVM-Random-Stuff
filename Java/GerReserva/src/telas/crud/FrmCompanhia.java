    private void tblCompanhiaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tblCompanhiaKeyPressed
	int val = tblCompanhia.getSelectedRow();
	if (evt.getID() == java.awt.event.KeyEvent.VK_DOWN) {
	    if (val <= tblCompanhia.getRowCount()) {
		val++;
	    }
	} else if (evt.getID() == java.awt.event.KeyEvent.VK_UP) {
	    if (val > 0) {
		val--;
	    }
	} else {
	    return;
	}
	tblCompanhiaSelect(val);
    }//GEN-LAST:event_tblCompanhiaKeyPressed

